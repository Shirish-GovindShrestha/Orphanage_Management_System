    private void startProgress() {
        javax.swing.SwingWorker<Void, Integer> worker = new javax.swing.SwingWorker<>() {
            @Override
            protected Void doInBackground() throws Exception {
                for (int i = 0; i <= 100; i++) {
                    Thread.sleep(30); // Simulated delay for progress bar
                    publish(i); // Publish progress
                }
                return null;
            }

            @Override
            protected void process(java.util.List<Integer> chunks) {
                int progress = chunks.get(chunks.size() - 1);
                progressBar.setValue(progress);
            }

            @Override
            protected void done() {
                loadScreen("LoginScreen"); // Switch to login screen

            }
        };
        worker.execute(); // Start the worker thread
    }

    private void refreshTable() {
        DefaultTableModel table = (DefaultTableModel) orphanageTb.getModel();

        // Clear existing rows if needed
        table.setRowCount(0);

        // Populate the table with student data
        this.orphanList.forEach((id, orphan) -> table.addRow(new Object[]{
            orphan.getOrphanID(),
            orphan.getFirstName(),
            orphan.getLastName(),
            orphan.getAge(),
            orphan.getWeight(),
            orphan.getHeight(),
            orphan.getGender(),
            orphan.getBloodGroup()
        }));

    }

